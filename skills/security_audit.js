// Skill Definition
// ═══════════════════════════════════════════════════════════════════════════════

export default {
  name: "security_audit",
  description:
    "Comprehensive security audit. Scans the codebase for hardcoded secrets, " +
    "checks dependency vulnerabilities via npm audit, runs OWASP Top 10 analysis, " +
    "and reviews MCP server security configurations. " +
    "Generates a structured security report with exploit scenarios and fix recommendations. " +
    "Actions: scan (run full audit), quick (fast secret scan only), report (show last report).",

  parameters: {
    type: "object",
    properties: {
      action: {
        type: "string",
        enum: ["scan", "quick", "report"],
        description:
          "Action to perform:\n" +
          "- 'scan' → Run full security audit (secrets + dependencies + OWASP + MCP)\n" +
          "- 'quick' → Fast scan: secrets only (no npm audit, no OWASP)\n" +
          "- 'report' → Show the last security report",
      },
      path: {
        type: "string",
        description:
          "Project path to scan. Defaults to the current project root. " +
          "Example: /path/to/project",
      },
      confidenceThreshold: {
        type: "number",
        description:
          "Minimum confidence threshold (0.0 - 1.0). Findings below this threshold are filtered out. " +
          "Default: 0.7 (gstack's 8/10 confidence gate equivalent).",
      },
    },
    required: ["action"],
  },

  handler: async (params, options = {}) => {
    const { action, path: scanPath, confidenceThreshold } = params;

    // Store last report globally
    if (!global.__security_last_report) {
      global.__security_last_report = null;
    }

    switch (action) {
      case "scan":
      case "quick": {
        const rootDir = scanPath
          ? path.resolve(scanPath)
          : path.resolve(__dirname, "..");

        const isQuick = action === "quick";

        const result = await runSecurityScan(rootDir, {
          scanSecrets: true,
          scanDependencies: !isQuick,
          scanOWASP: !isQuick,
          scanMCP: !isQuick,
          confidenceThreshold: confidenceThreshold || 0.7,
        });

        // Generate report
        const report = generateReport(result);

        // Store for later retrieval
        global.__security_last_report = {
          summary: result.summary,
          report,
          timestamp: result.scannedAt,
        };

        return {
          success: true,
          summary: result.summary,
          report,
          isQuick,
          findingsCount: result.findings.length,
          message: isQuick
            ? `Quick security scan complete: ${result.summary.total} findings (${result.summary.bySeverity.HIGH} HIGH)`
            : `Full security audit complete: ${result.summary.total} findings (${result.summary.bySeverity.HIGH} HIGH, ${result.summary.bySeverity.MEDIUM} MEDIUM, ${result.summary.bySeverity.LOW} LOW)`,
        };
      }

      case "report": {
        if (!global.__security_last_report) {
          return {
            success: true,
            message: "No security audit has been run yet. Use action: 'scan' to run one.",
            report: null,
          };
        }
        return {
          success: true,
          ...global.__security_last_report,
        };
      }

      default:
        return { success: false, error: `Unknown action: "${action}"` };
    }
  },
};
