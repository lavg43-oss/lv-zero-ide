/**
 * Generates a structured markdown security report from scan findings.
 *
 * @param {object} scanResult - Result from runSecurityScan()
 * @returns {string} Markdown report
 */
function generateReport(scanResult) {
  const { summary, findings } = scanResult;
  const now = new Date().toISOString().split("T")[0];

  let report = `# 🔒 Security Audit Report\n\n`;
  report += `**Date:** ${now}\n`;
  report += `**Findings:** ${summary.total} total (${summary.bySeverity.HIGH} HIGH, ${summary.bySeverity.MEDIUM} MEDIUM, ${summary.bySeverity.LOW} LOW)\n\n`;

  // Executive Summary
  report += `## Executive Summary\n\n`;
  report += `| Severity | Count |\n|----------|-------|\n`;
  report += `| 🔴 HIGH | ${summary.bySeverity.HIGH} |\n`;
  report += `| 🟡 MEDIUM | ${summary.bySeverity.MEDIUM} |\n`;
  report += `| 🟢 LOW | ${summary.bySeverity.LOW} |\n`;
  report += `| **Total** | **${summary.total}** |\n\n`;

  // By Category
  report += `### By Category\n\n`;
  for (const [cat, count] of Object.entries(summary.byCategory)) {
    report += `- **${cat}**: ${count}\n`;
  }
  report += "\n";

  // HIGH severity findings
  const highFindings = findings.filter((f) => f.severity === "HIGH");
  if (highFindings.length > 0) {
    report += `## 🔴 High Severity Findings\n\n`;
    for (const f of highFindings) {
      report += `### ${f.label}\n\n`;
      report += `- **File:** \`${f.file}:${f.line}\`\n`;
      report += `- **Match:** \`${f.match}\`\n`;
      report += `- **Confidence:** ${Math.round(f.confidence * 100)}%\n`;
      report += `- **Category:** ${f.category}\n\n`;

      // Generate exploit scenario
      report += `**Exploit Scenario:**\n`;
      report += `${generateExploitScenario(f)}\n\n`;

      // Fix recommendation
      if (f.fix) {
        report += `**Recommended Fix:** ${f.fix}\n\n`;
      }

      report += `---\n\n`;
    }
  }

  // MEDIUM severity findings
  const medFindings = findings.filter((f) => f.severity === "MEDIUM");
  if (medFindings.length > 0) {
    report += `## 🟡 Medium Severity Findings\n\n`;
    report += `| # | Issue | File | Line | Confidence |\n|---|-------|------|------|------------|\n`;
    medFindings.forEach((f, i) => {
      report += `| ${i + 1} | ${f.label} | \`${f.file}\` | ${f.line} | ${Math.round(f.confidence * 100)}% |\n`;
    });
    report += "\n";
  }

  // LOW severity findings
  const lowFindings = findings.filter((f) => f.severity === "LOW");
  if (lowFindings.length > 0) {
    report += `## 🟢 Low Severity Findings\n\n`;
    report += `| # | Issue | File | Line |\n|---|-------|------|------|\n`;
    lowFindings.forEach((f, i) => {
      report += `| ${i + 1} | ${f.label} | \`${f.file}\` | ${f.line} |\n`;
    });
    report += "\n";
  }

  // Recommendations
  report += `## Recommendations\n\n`;
  report += `1. **Fix HIGH severity issues immediately** — ${highFindings.length} finding(s) require immediate attention\n`;
  report += `2. **Address MEDIUM severity issues this sprint** — ${medFindings.length} finding(s) should be fixed\n`;
  report += `3. **Review LOW severity issues when convenient** — ${lowFindings.length} finding(s) to consider\n`;
  if (findings.some((f) => f.category === "dependencies")) {
    report += `4. **Run \`npm audit fix\`** to address dependency vulnerabilities\n`;
  }
  if (findings.some((f) => f.category === "mcp_security")) {
    report += `5. **Review MCP server configurations** — ensure no hardcoded secrets or dangerous commands\n`;
  }
  report += "\n";

  // Footer
  report += `---\n*Report generated by lv-zero Security Audit on ${now}*\n`;

  return report;
}

