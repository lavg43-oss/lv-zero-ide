// ─── Pipeline Runner ─────────────────────────────────────────────────────────

/**
 * Run the full spec generation pipeline.
 * Creates all 4 spec files in the project root.
 * Each file is wrapped in try/catch — failures don't block other files.
 *
 * @param {string} projectPath — Path to the project
 * @param {object} identity — Project identity config
 * @param {object} [context] — Optional context awareness data
 * @param {object} [scopingAnswers] — Optional scoping answers from Grill Me wizard
 * @returns {{ generated: string[], errors: string[] }}
 */
function runSpecPipeline(projectPath, identity, context, scopingAnswers) {
  const generated = [];
  const errors = [];

  const generators = [
    { name: SPEC_FILES.PROJECT, fn: () => generateProjectMd(identity, context, scopingAnswers) },
    { name: SPEC_FILES.REQUIREMENTS, fn: () => generateRequirementsMd(identity, scopingAnswers) },
    { name: SPEC_FILES.ROADMAP, fn: () => generateRoadmapMd(identity, scopingAnswers) },
    { name: SPEC_FILES.TECH_SPEC, fn: () => generateTechSpecMd(identity, context, scopingAnswers) },
  ];

  for (const { name, fn } of generators) {
    try {
      const content = fn();
      const filePath = path.join(projectPath, name);
      fs.writeFileSync(filePath, content, "utf8");
      generated.push(name);
      console.log(`[SpecGenerator] Created ${name}`);
    } catch (err) {
      errors.push(`${name}: ${err.message}`);
      console.warn(`[SpecGenerator] Failed to create ${name}: ${err.message}`);
    }
  }

  return { generated, errors };
}

// ─── Status Check ────────────────────────────────────────────────────────────

/**
 * Check which spec files exist in a project.
 * @param {string} projectPath
 * @returns {{ complete: boolean, existing: string[], missing: string[] }}
 */
function getSpecStatus(projectPath) {
  const existing = [];
  const missing = [];

  for (const key of Object.keys(SPEC_FILES)) {
    const filePath = path.join(projectPath, SPEC_FILES[key]);
    if (fs.existsSync(filePath)) {
      existing.push(SPEC_FILES[key]);
    } else {
      missing.push(SPEC_FILES[key]);
    }
  }

  return {
    complete: missing.length === 0,
    existing,
    missing,
  };
}

// ─── Read Spec File ──────────────────────────────────────────────────────────

/**
 * Read a spec file by type.
 * @param {string} projectPath
 * @param {string} specType — One of "PROJECT.md", "REQUIREMENTS.md", "ROADMAP.md", "TECH-SPEC.md"
 * @returns {string|null} — File content or null if not found
 */
function readSpecFile(projectPath, specType) {
  const filePath = path.join(projectPath, specType);
  try {
    if (fs.existsSync(filePath)) {
      return fs.readFileSync(filePath, "utf8");
    }
    return null;
  } catch (err) {
    console.warn(`[SpecGenerator] Could not read ${specType}: ${err.message}`);
    return null;
  }
}

