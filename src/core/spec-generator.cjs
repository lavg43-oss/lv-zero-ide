/**
 * lv-zero — Spec Generator Module (Phase 4)
 *
 * Generates 4 spec files (PROJECT.md, REQUIREMENTS.md, ROADMAP.md, TECH-SPEC.md)
 * from project identity and context awareness data.
 *
 * v1.0 — Spec-First Init Pipeline
 *   Each generator function is independent — failure of one doesn't affect others.
 */

const path = require("path");
const fs = require("fs");

// ─── Spec File Names ─────────────────────────────────────────────────────────
const SPEC_FILES = {
  PROJECT: "PROJECT.md",
  REQUIREMENTS: "REQUIREMENTS.md",
  ROADMAP: "ROADMAP.md",
  TECH_SPEC: "TECH-SPEC.md",
};

// ─── Template Generators ─────────────────────────────────────────────────────

/**
 * Generate PROJECT.md content from project identity.
 * @param {object} identity — Project identity config from project-identity.cjs
 * @param {object} [context] — Optional context awareness data (frameworks, languages, etc.)
 * @param {object} [scopingAnswers] — Optional scoping answers from Grill Me wizard
 * @returns {string} — Markdown content
 */
function generateProjectMd(identity, context, scopingAnswers) {
  const name = identity.name || "My Project";
  const type = identity.type || "desktop";
  const stage = identity.stage || "prototype";
  const languages = identity.languages || [];
  const frameworks = identity.frameworks || [];
  const platform = identity.platform || "electron";
  const langList = languages.length > 0 ? languages.join(", ") : "TBD";
  const fwList = frameworks.length > 0 ? frameworks.join(", ") : "None yet";

  // ── Inject scoping answers when available ──────────────────────────────
  const scopeProblem = scopingAnswers && scopingAnswers.problem
    ? scopingAnswers.problem
    : null;
  const scopeUsers = scopingAnswers && scopingAnswers.users
    ? scopingAnswers.users
    : null;
  const scopeCoreFeature = scopingAnswers && scopingAnswers.core_feature
    ? scopingAnswers.core_feature
    : null;

  const scopeDescription = [scopeProblem, scopeUsers, scopeCoreFeature]
    .filter(Boolean)
    .join("\n\n");

  return `# ${name}

> **Type:** ${type}
> **Stage:** ${stage}
> **Platform:** ${platform}

## Description

${scopeDescription ? scopeDescription : "<!-- Add your project description here -->"}

${scopeDescription ? "" : `A ${type} project built with ${langList}${frameworks.length > 0 ? ` using ${fwList}` : ""}.`}

## Tech Stack

| Category | Technology |
|----------|-----------|
| **Languages** | ${langList} |
| **Frameworks** | ${fwList} |
| **Platform** | ${platform} |

${context && context.srcDirs && context.srcDirs.length > 0 ? `## Directory Structure

\`\`\`
${context.srcDirs.map(d => `├── ${d}/`).join("\n")}
└── ...
\`\`\`
` : ""}
${scopeUsers ? `## Target Users

${scopeUsers}
` : ""}
## Setup Instructions

<!-- Add setup instructions here -->

\`\`\`bash
# Example setup
npm install
npm run dev
\`\`\`

---
*Generated by lv-zero init pipeline — ${new Date().toISOString()}*
`;
}

